public class Basics {
    public static void main(String[] args) {
        //if condition

        int a = 10;
        if(a == 10){
            System.out.println("Hello World!");
        }
        //while loop
        int count = 1;
        while(count <= 5){
            System.out.print(count + " ");
            count++;
        }
        System.out.println(" ");
        //for loop
        for(int i = 0; i <= 5; i++){
            System.out.print(i + " ");
        }
    }
}
